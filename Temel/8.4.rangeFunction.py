# range(number) ---> bu fonksiyon içinde yazılan değere kadar  0'dan +1 yaparak gider

for x in range(100):
    print(x)            # çıktı---> 0 1 2 3 4 ... 99
    
    
    
print("\n***********\n")
    


for x in range(1,10):   # 1'den 10'a kadar
    print(x)            # çıktı---> 1 2 3 ... 9
    
    
    
print("\n***********\n")
        


for x in range(1,10,2):   # 1'den 10'a kadar
    print(x)              # çıktı---> 1 3 5 7 9